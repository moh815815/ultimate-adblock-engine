// مسار API: مقاييس الأداء
// Route: /api/performance
// Rule-Parsing Performance Overhead Calculator
// ======================================================
import { NextRequest, NextResponse } from "next/server";
import {
  prisma,
  التحقق_من_المستأجر,
  بناء_استجابة,
  بناء_استجابة_خطأ,
  استخراج_مفتاح_API,
} from "@/lib/prisma";
import {
  تجميع_قائمة_حجب_كاملة,
  تصنيف_أداء_التجميع,
  محاكاة_قائمة_EasyList_مصغرة,
} from "@/lib/rule-compiler";
import type { تقرير_أداء_التجميع } from "@/types/arabic-types";

// ======================================================
// GET /api/performance — ملخص مقاييس الأداء التاريخية
// ======================================================
export async function GET(request: NextRequest) {
  const بداية = Date.now();

  try {
    const { خطأ, رمز, مستأجر } = await التحقق_من_المستأجر(
      استخراج_مفتاح_API(request)
    );
    if (خطأ || !مستأجر) {
      return NextResponse.json(بناء_استجابة_خطأ(خطأ!, رمز, بداية), { status: رمز });
    }

    const { searchParams } = new URL(request.url);
    const أيام = Math.min(parseInt(searchParams.get("أيام") ?? "7"), 30);
    const من = new Date(Date.now() - أيام * 24 * 60 * 60 * 1000);

    const مقاييس = await prisma.مقياس_أداء.findMany({
      where: {
        معرف_المستأجر: مستأجر.معرف,
        الطابع_الزمني: { gte: من },
      },
      orderBy: { الطابع_الزمني: "desc" },
    });

    if (مقاييس.length === 0) {
      return NextResponse.json(
        بناء_استجابة(
          { رسالة: "لا توجد مقاييس أداء للفترة المحددة", نصيحة: "نفّذ مزامنة أولاً عبر POST /api/sync" },
          "لا توجد بيانات أداء",
          200,
          بداية
        ),
        { status: 200 }
      );
    }

    // حسابات إحصائية
    const متوسطات_الوقت = مقاييس.map((م) => م.متوسط_وقت_قاعدة_مكرو);
    const متوسط_عام = متوسطات_الوقت.reduce((أ, ب) => أ + ب, 0) / متوسطات_الوقت.length;
    const ذاكرة = مقاييس.map((م) => م.استهلاك_الذاكرة_KB);

    // توزيع تصنيفات الأداء
    const توزيع_التصنيفات = مقاييس.reduce(
      (توزيع, م) => {
        const تصنيف = تصنيف_أداء_التجميع(
          م.متوسط_وقت_قاعدة_مكرو,
          م.نسبة_ضغط_القواعد * 100
        );
        توزيع[تصنيف] = (توزيع[تصنيف] ?? 0) + 1;
        return توزيع;
      },
      {} as Record<string, number>
    );

    // اتجاه الأداء (مقارنة النصف الأول بالثاني)
    const نصف = Math.floor(مقاييس.length / 2);
    const متوسط_حديث = نصف > 0
      ? متوسطات_الوقت.slice(0, نصف).reduce((أ, ب) => أ + ب, 0) / نصف
      : متوسط_عام;
    const متوسط_قديم = نصف > 0
      ? متوسطات_الوقت.slice(نصف).reduce((أ, ب) => أ + ب, 0) / Math.max(مقاييس.length - نصف, 1)
      : متوسط_عام;
    const نسبة_التحسن = متوسط_قديم > 0
      ? Math.round(((متوسط_قديم - متوسط_حديث) / متوسط_قديم) * 100)
      : 0;

    const تقرير = {
      الفترة_الزمنية: {
        من: من.toISOString(),
        إلى: new Date().toISOString(),
        عدد_الأيام: أيام,
        عدد_عمليات_التجميع: مقاييس.length,
      },
      إحصائيات_الوقت: {
        متوسط_وقت_قاعدة_مكرو: Math.round(متوسط_عام * 100) / 100,
        أسرع_قاعدة_مكرو: Math.min(...متوسطات_الوقت),
        أبطأ_قاعدة_مكرو: Math.max(...متوسطات_الوقت),
        انحراف_معياري: Math.round(
          Math.sqrt(
            متوسطات_الوقت.reduce((م, و) => م + Math.pow(و - متوسط_عام, 2), 0) /
              متوسطات_الوقت.length
          ) * 100
        ) / 100,
      },
      إحصائيات_القواعد: {
        إجمالي_قواعد_مُعالجة: مقاييس.reduce((م, ق) => م + ق.عدد_القواعد_المُعالَجة, 0),
        إجمالي_قواعد_DNR_منتجة: مقاييس.reduce((م, ق) => م + ق.قواعد_DNR_المُنتجة, 0),
        متوسط_نسبة_الضغط:
          Math.round(
            (مقاييس.reduce((م, ق) => م + ق.نسبة_ضغط_القواعد, 0) / مقاييس.length) * 100
          ) / 100,
      },
      استهلاك_الموارد: {
        متوسط_ذاكرة_KB: Math.round(ذاكرة.reduce((أ, ب) => أ + ب, 0) / ذاكرة.length),
        ذروة_الذاكرة_KB: Math.max(...ذاكرة),
        إجمالي_وقت_تجميع_مللي: مقاييس.reduce((م, ق) => م + ق.وقت_التجميع_كلي_مللي, 0),
      },
      توزيع_تصنيفات_الأداء: توزيع_التصنيفات,
      اتجاه_الأداء: {
        متوسط_حديث_مكرو: Math.round(متوسط_حديث * 100) / 100,
        متوسط_قديم_مكرو: Math.round(متوسط_قديم * 100) / 100,
        نسبة_التحسن_بالمئة: نسبة_التحسن,
        الاتجاه: نسبة_التحسن > 0 ? "تحسن" : نسبة_التحسن < 0 ? "تراجع" : "ثابت",
      },
      تحذيرات_إجمالية: مقاييس.flatMap((م) => م.تحذيرات_التحقق).slice(0, 20),
    };

    return NextResponse.json(
      بناء_استجابة(تقرير, "تم جلب تقرير الأداء بنجاح", 200, بداية),
      { status: 200 }
    );
  } catch (خطأ) {
    console.error("[أداء:GET]", خطأ);
    return NextResponse.json(
      بناء_استجابة_خطأ("خطأ في جلب مقاييس الأداء", 500, بداية, String(خطأ)),
      { status: 500 }
    );
  }
}

// ======================================================
// POST /api/performance/benchmark — معيار الأداء المباشر
// ======================================================
export async function POST(request: NextRequest) {
  const بداية = Date.now();

  try {
    const { خطأ, رمز, مستأجر } = await التحقق_من_المستأجر(
      استخراج_مفتاح_API(request)
    );
    if (خطأ || !مستأجر) {
      return NextResponse.json(بناء_استجابة_خطأ(خطأ!, رمز, بداية), { status: رمز });
    }

    let الجسم: { محتوى_قائمة_مخصصة?: string; عدد_التكرارات?: number } = {};
    try { الجسم = await request.json(); } catch { /* استخدام الافتراضيات */ }

    const محتوى = الجسم.محتوى_قائمة_مخصصة ?? محاكاة_قائمة_EasyList_مصغرة();
    const تكرارات = Math.min(الجسم.عدد_التكرارات ?? 3, 10);

    // تشغيل اختبار الأداء عدة مرات
    const نتائج_التكرارات = [];
    for (let i = 0; i < تكرارات; i++) {
      const نتيجة = تجميع_قائمة_حجب_كاملة(محتوى);
      نتائج_التكرارات.push(نتيجة);
    }

    const أوقات_التجميع = نتائج_التكرارات.map((ن) => ن.إحصائيات.وقت_التجميع_الكلي_مللي);
    const أوقات_القاعدة = نتائج_التكرارات.map((ن) => ن.إحصائيات.متوسط_وقت_قاعدة_مكرو);

    const آخر_نتيجة = نتائج_التكرارات[نتائج_التكرارات.length - 1];
    const إجمالي_قواعد_معالجة =
      آخر_نتيجة.إحصائيات.قواعد_شبكة +
      آخر_نتيجة.إحصائيات.قواعد_استثناء +
      آخر_نتيجة.إحصائيات.قواعد_مرفوضة;

    const نسبة_نجاح =
      إجمالي_قواعد_معالجة > 0
        ? ((آخر_نتيجة.إحصائيات.قواعد_شبكة + آخر_نتيجة.إحصائيات.قواعد_استثناء) /
            إجمالي_قواعد_معالجة) *
          100
        : 0;

    const تقرير: تقرير_أداء_التجميع = {
      معرف_العملية: `bench_${Date.now()}`,
      إحصائيات_الوقت: {
        إجمالي_وقت_التجميع_مللي: أوقات_التجميع.reduce((أ, ب) => أ + ب, 0) / تكرارات,
        متوسط_وقت_قاعدة_مكرو: أوقات_القاعدة.reduce((أ, ب) => أ + ب, 0) / تكرارات,
        أسرع_قاعدة_مكرو: آخر_نتيجة.إحصائيات.أسرع_قاعدة_مكرو,
        أبطأ_قاعدة_مكرو: آخر_نتيجة.إحصائيات.أبطأ_قاعدة_مكرو,
        انحراف_معياري_مكرو: آخر_نتيجة.إحصائيات.انحراف_معياري_مكرو,
      },
      إحصائيات_القواعد: {
        إجمالي_القواعد_المُعالجة: آخر_نتيجة.إحصائيات.إجمالي_الأسطر,
        قواعد_شبكة_ناجحة: آخر_نتيجة.إحصائيات.قواعد_شبكة,
        قواعد_تجميل_ناجحة: آخر_نتيجة.إحصائيات.قواعد_تجميل,
        قواعد_مرفوضة: آخر_نتيجة.إحصائيات.قواعد_مرفوضة,
        نسبة_نجاح_التجميع: Math.round(نسبة_نجاح * 100) / 100,
      },
      كفاءة_الذاكرة: {
        استهلاك_الذاكرة_قبل_KB: Math.round(process.memoryUsage().heapUsed / 1024),
        استهلاك_الذاكرة_بعد_KB: Math.round(process.memoryUsage().heapUsed / 1024),
        نسبة_ضغط_القواعد:
          Math.round(
            (آخر_نتيجة.قواعد_DNR.length / Math.max(آخر_نتيجة.إحصائيات.إجمالي_الأسطر, 1)) * 100
          ) / 100,
        تقليل_الحجم_بالمئة: Math.round(
          (1 - آخر_نتيجة.قواعد_DNR.length / Math.max(آخر_نتيجة.إحصائيات.إجمالي_الأسطر, 1)) * 100
        ),
      },
      مخرجات_DNR: {
        قواعد_حجب_شبكة: آخر_نتيجة.قواعد_DNR.filter((ق) => ق.action.type === "block").length,
        قواعد_إعادة_توجيه: آخر_نتيجة.قواعد_DNR.filter((ق) => ق.action.type === "redirect").length,
        قواعد_تعديل_رأس: آخر_نتيجة.قواعد_DNR.filter((ق) => ق.action.type === "modifyHeaders").length,
        قواعد_تجميل_ترحيل: آخر_نتيجة.إحصائيات.قواعد_تجميل,
      },
      تحذيرات: آخر_نتيجة.تحذيرات,
      تصنيف_الأداء: تصنيف_أداء_التجميع(
        أوقات_القاعدة.reduce((أ, ب) => أ + ب, 0) / تكرارات,
        نسبة_نجاح
      ),
    };

    return NextResponse.json(
      بناء_استجابة(
        {
          تقرير_الأداء: تقرير,
          تفاصيل_التكرارات: نتائج_التكرارات.map((ن, فهرس) => ({
            رقم_التكرار: فهرس + 1,
            وقت_التجميع_مللي: ن.إحصائيات.وقت_التجميع_الكلي_مللي,
            قواعد_DNR_منتجة: ن.قواعد_DNR.length,
          })),
        },
        "اكتمل اختبار الأداء بنجاح",
        200,
        بداية
      ),
      { status: 200 }
    );
  } catch (خطأ) {
    console.error("[أداء:POST]", خطأ);
    return NextResponse.json(
      بناء_استجابة_خطأ("خطأ في تشغيل اختبار الأداء", 500, بداية, String(خطأ)),
      { status: 500 }
    );
  }
}
