// ======================================================
// مسار API: قوائم الحجب
// Route: /api/blocklists
// ======================================================
import { NextRequest, NextResponse } from "next/server";
import {
  prisma,
  التحقق_من_المستأجر,
  بناء_استجابة,
  بناء_استجابة_خطأ,
  استخراج_مفتاح_API,
} from "@/lib/prisma";
import type { طلب_إنشاء_قائمة } from "@/types/arabic-types";

// ======================================================
// GET /api/blocklists — جلب قوائم الحجب للمستأجر
// ======================================================
export async function GET(request: NextRequest) {
  const بداية = Date.now();

  try {
    const { خطأ, رمز, مستأجر } = await التحقق_من_المستأجر(
      استخراج_مفتاح_API(request)
    );

    if (خطأ || !مستأجر) {
      return NextResponse.json(
        بناء_استجابة_خطأ(خطأ!, رمز, بداية),
        { status: رمز }
      );
    }

    const { searchParams } = new URL(request.url);
    const صفحة = parseInt(searchParams.get("صفحة") ?? "1");
    const حجم = Math.min(parseInt(searchParams.get("حجم") ?? "20"), 100);
    const فلتر_الحالة = searchParams.get("الحالة");
    const فلتر_النوع = searchParams.get("النوع");

    const شروط_التصفية: Record<string, unknown> = {
      معرف_المستأجر: مستأجر.معرف,
      ...(فلتر_الحالة && { الحالة: فلتر_الحالة }),
      ...(فلتر_النوع && { النوع: فلتر_النوع }),
    };

    const [قوائم, المجموع] = await prisma.$transaction([
      prisma.قائمة_الحجب.findMany({
        where: شروط_التصفية,
        include: {
          _count: { select: { القواعد: { where: { نشطة: true } } } },
          مزامنات: {
            orderBy: { تاريخ_البدء: "desc" },
            take: 1,
            select: {
              حالة_المزامنة: true,
              تاريخ_البدء: true,
              تاريخ_الانتهاء: true,
              قواعد_مضافة: true,
              قواعد_محذوفة: true,
            },
          },
        },
        orderBy: { تاريخ_التحديث: "desc" },
        skip: (صفحة - 1) * حجم,
        take: حجم,
      }),
      prisma.قائمة_الحجب.count({ where: شروط_التصفية }),
    ]);

    const بيانات_المنسقة = قوائم.map((قائمة) => ({
      المعرف: قائمة.معرف,
      الاسم: قائمة.الاسم,
      الوصف: قائمة.الوصف,
      رابط_المصدر: قائمة.رابط_المصدر,
      النوع: قائمة.النوع,
      الحالة: قائمة.الحالة,
      إحصائيات: {
        إجمالي_القواعد: قائمة.عدد_القواعد,
        القواعد_النشطة: قائمة._count.القواعد,
        حجم_الملف_KB: Math.round(Number(قائمة.حجم_الملف_بايت) / 1024),
        إصدار_EasyList: قائمة.إصدار_EasyList,
      },
      آخر_مزامنة: قائمة.آخر_مزامنة?.toISOString() ?? null,
      تكرار_المزامنة_ثانية: قائمة.تكرار_المزامنة,
      أحدث_مزامنة: قائمة.مزامنات[0] ?? null,
      تواريخ: {
        الإنشاء: قائمة.تاريخ_الإنشاء.toISOString(),
        التحديث: قائمة.تاريخ_التحديث.toISOString(),
      },
    }));

    const استجابة = {
      ...بناء_استجابة(
        بيانات_المنسقة,
        `تم جلب ${قوائم.length} قائمة حجب بنجاح`,
        200,
        بداية
      ),
      التقسيم: {
        رقم_الصفحة: صفحة,
        حجم_الصفحة: حجم,
        إجمالي_النتائج: المجموع,
        إجمالي_الصفحات: Math.ceil(المجموع / حجم),
        توجد_صفحة_تالية: صفحة * حجم < المجموع,
      },
    };

    return NextResponse.json(استجابة, { status: 200 });
  } catch (خطأ) {
    console.error("[قوائم_الحجب:GET]", خطأ);
    return NextResponse.json(
      بناء_استجابة_خطأ("خطأ داخلي في الخادم", 500, بداية, String(خطأ)),
      { status: 500 }
    );
  }
}

// ======================================================
// POST /api/blocklists — إنشاء قائمة حجب جديدة
// ======================================================
export async function POST(request: NextRequest) {
  const بداية = Date.now();

  try {
    const { خطأ, رمز, مستأجر } = await التحقق_من_المستأجر(
      استخراج_مفتاح_API(request)
    );

    if (خطأ || !مستأجر) {
      return NextResponse.json(
        بناء_استجابة_خطأ(خطأ!, رمز, بداية),
        { status: رمز }
      );
    }

    // التحقق من حدود الخطة
    const عدد_القوائم_الحالي = await prisma.قائمة_الحجب.count({
      where: { معرف_المستأجر: مستأجر.معرف },
    });

    const حدود_الخطة = { مجاني: 3, احترافي: 25, مؤسسي: 1000 };
    const الحد = حدود_الخطة[مستأجر.الخطة];

    if (عدد_القوائم_الحالي >= الحد) {
      return NextResponse.json(
        بناء_استجابة_خطأ(
          `وصلت إلى الحد الأقصى لقوائم الحجب في خطة "${مستأجر.الخطة}" (${الحد} قوائم). يرجى الترقية.`,
          402,
          بداية
        ),
        { status: 402 }
      );
    }

    let الجسم: طلب_إنشاء_قائمة;
    try {
      الجسم = await request.json();
    } catch {
      return NextResponse.json(
        بناء_استجابة_خطأ("جسم الطلب غير صالح - يجب أن يكون JSON", 400, بداية),
        { status: 400 }
      );
    }

    // التحقق من الحقول المطلوبة
    if (!الجسم.الاسم || !الجسم.رابط_المصدر || !الجسم.النوع) {
      return NextResponse.json(
        بناء_استجابة_خطأ(
          "الحقول المطلوبة مفقودة: الاسم، رابط_المصدر، النوع",
          400,
          بداية
        ),
        { status: 400 }
      );
    }

    // التحقق من صحة URL
    try {
      new URL(الجسم.رابط_المصدر);
    } catch {
      return NextResponse.json(
        بناء_استجابة_خطأ("رابط المصدر غير صالح", 400, بداية),
        { status: 400 }
      );
    }

    const قائمة_جديدة = await prisma.قائمة_الحجب.create({
      data: {
        معرف_المستأجر: مستأجر.معرف,
        الاسم: الجسم.الاسم,
        الوصف: الجسم.الوصف,
        رابط_المصدر: الجسم.رابط_المصدر,
        النوع: الجسم.النوع as never,
        الحالة: "معلقة",
        تكرار_المزامنة: الجسم.تكرار_المزامنة_ثانية ?? 3600,
      },
    });

    return NextResponse.json(
      بناء_استجابة(
        {
          المعرف: قائمة_جديدة.معرف,
          الاسم: قائمة_جديدة.الاسم,
          الحالة: قائمة_جديدة.الحالة,
          تاريخ_الإنشاء: قائمة_جديدة.تاريخ_الإنشاء.toISOString(),
          الخطوة_التالية: "استخدم POST /api/sync لبدء المزامنة الأولى",
        },
        `تم إنشاء قائمة الحجب "${قائمة_جديدة.الاسم}" بنجاح`,
        201,
        بداية
      ),
      { status: 201 }
    );
  } catch (خطأ) {
    console.error("[قوائم_الحجب:POST]", خطأ);
    return NextResponse.json(
      بناء_استجابة_خطأ("خطأ داخلي في الخادم", 500, بداية, String(خطأ)),
      { status: 500 }
    );
  }
}
