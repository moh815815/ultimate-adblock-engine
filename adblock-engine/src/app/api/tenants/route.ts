// ======================================================
// مسار API: إدارة المستأجرين
// Route: /api/tenants
// ======================================================
import { NextRequest, NextResponse } from "next/server";
import { randomBytes } from "crypto";
import {
  prisma,
  التحقق_من_المستأجر,
  بناء_استجابة,
  بناء_استجابة_خطأ,
  استخراج_مفتاح_API,
} from "@/lib/prisma";

// ======================================================
// POST /api/tenants — تسجيل مستأجر جديد (عملية إدارية)
// ======================================================
export async function POST(request: NextRequest) {
  const بداية = Date.now();

  // التحقق من مفتاح الإدارة
  const مفتاح_الإدارة = request.headers.get("X-Admin-Key");
  if (مفتاح_الإدارة !== process.env.ADMIN_SECRET_KEY) {
    return NextResponse.json(
      بناء_استجابة_خطأ("غير مصرح — مفتاح الإدارة غير صالح", 401, بداية),
      { status: 401 }
    );
  }

  try {
    let الجسم: { الاسم: string; الخطة?: string; الإعدادات?: Record<string, unknown> };
    try {
      الجسم = await request.json();
    } catch {
      return NextResponse.json(
        بناء_استجابة_خطأ("جسم الطلب غير صالح", 400, بداية),
        { status: 400 }
      );
    }

    if (!الجسم.الاسم?.trim()) {
      return NextResponse.json(
        بناء_استجابة_خطأ("حقل الاسم مطلوب", 400, بداية),
        { status: 400 }
      );
    }

    // توليد مفتاح API آمن
    const مفتاح_API = `adb_${randomBytes(32).toString("hex")}`;

    const مستأجر_جديد = await prisma.مستأجر.create({
      data: {
        الاسم: الجسم.الاسم.trim(),
        المفتاح_السري: مفتاح_API,
        الخطة: (الجسم.الخطة as never) ?? "مجاني",
        الإعدادات: الجسم.الإعدادات ?? {},
      },
    });

    return NextResponse.json(
      بناء_استجابة(
        {
          المعرف: مستأجر_جديد.معرف,
          الاسم: مستأجر_جديد.الاسم,
          الخطة: مستأجر_جديد.الخطة,
          مفتاح_API,
          تحذير: "احفظ مفتاح API هذا بأمان — لن يُعرض مجدداً",
          حدود_الخطة: {
            قوائم_حجب: { مجاني: 3, احترافي: 25, مؤسسي: 1000 }[مستأجر_جديد.الخطة],
            حجم_دفعة_السجلات: { مجاني: 100, احترافي: 1000, مؤسسي: 10000 }[مستأجر_جديد.الخطة],
          },
          تاريخ_الإنشاء: مستأجر_جديد.تاريخ_الإنشاء.toISOString(),
        },
        `تم إنشاء المستأجر "${مستأجر_جديد.الاسم}" بنجاح`,
        201,
        بداية
      ),
      { status: 201 }
    );
  } catch (خطأ) {
    console.error("[مستأجرون:POST]", خطأ);
    return NextResponse.json(
      بناء_استجابة_خطأ("خطأ في إنشاء المستأجر", 500, بداية, String(خطأ)),
      { status: 500 }
    );
  }
}

// ======================================================
// GET /api/tenants/me — معلومات المستأجر الحالي
// ======================================================
export async function GET(request: NextRequest) {
  const بداية = Date.now();

  try {
    const { خطأ, رمز, مستأجر } = await التحقق_من_المستأجر(
      استخراج_مفتاح_API(request)
    );
    if (خطأ || !مستأجر) {
      return NextResponse.json(بناء_استجابة_خطأ(خطأ!, رمز, بداية), { status: رمز });
    }

    // جلب إحصائيات المستأجر
    const [
      عدد_القوائم,
      عدد_القواعد,
      آخر_مزامنة,
      إجمالي_سجلات_الانحراف,
      إجمالي_محجوب_اليوم,
    ] = await prisma.$transaction([
      prisma.قائمة_الحجب.count({
        where: { معرف_المستأجر: مستأجر.معرف, الحالة: "نشطة" },
      }),
      prisma.قاعدة_تصفية.count({
        where: {
          القائمة: { معرف_المستأجر: مستأجر.معرف },
          نشطة: true,
        },
      }),
      prisma.سجل_مزامنة.findFirst({
        where: { معرف_المستأجر: مستأجر.معرف, حالة_المزامنة: "مكتملة" },
        orderBy: { تاريخ_الانتهاء: "desc" },
        select: { تاريخ_الانتهاء: true },
      }),
      prisma.سجل_انحراف.count({
        where: { معرف_المستأجر: مستأجر.معرف },
      }),
      prisma.سجل_انحراف.count({
        where: {
          معرف_المستأجر: مستأجر.معرف,
          الإجراء: "محجوب" as never,
          الطابع_الزمني: { gte: new Date(Date.now() - 24 * 60 * 60 * 1000) },
        },
      }),
    ]);

    return NextResponse.json(
      بناء_استجابة(
        {
          المعرف: مستأجر.معرف,
          الاسم: مستأجر.الاسم,
          الخطة: مستأجر.الخطة,
          الإحصائيات: {
            قوائم_الحجب_النشطة: عدد_القوائم,
            إجمالي_القواعد_النشطة: عدد_القواعد,
            إجمالي_سجلات_الانحراف,
            محجوب_في_آخر_24_ساعة: إجمالي_محجوب_اليوم,
            آخر_مزامنة: آخر_مزامنة?.تاريخ_الانتهاء?.toISOString() ?? null,
          },
          حدود_الخطة: {
            قوائم_حجب: { مجاني: 3, احترافي: 25, مؤسسي: 1000 }[مستأجر.الخطة],
            حجم_دفعة_السجلات: { مجاني: 100, احترافي: 1000, مؤسسي: 10000 }[مستأجر.الخطة],
          },
          الإعدادات: مستأجر.الإعدادات,
          تاريخ_الانضمام: مستأجر.تاريخ_الإنشاء.toISOString(),
        },
        "تم جلب معلومات المستأجر بنجاح",
        200,
        بداية
      ),
      { status: 200 }
    );
  } catch (خطأ) {
    console.error("[مستأجرون:GET]", خطأ);
    return NextResponse.json(
      بناء_استجابة_خطأ("خطأ في جلب معلومات المستأجر", 500, بداية, String(خطأ)),
      { status: 500 }
    );
  }
}
