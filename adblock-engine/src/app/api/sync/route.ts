// ======================================================
// مسار API: المزامنة التلقائية لقوائم الحجب
// Route: /api/sync
// Multi-tenant Blocklist Synchronization Engine
// ======================================================
import { NextRequest, NextResponse } from "next/server";
import {
  prisma,
  التحقق_من_المستأجر,
  بناء_استجابة,
  بناء_استجابة_خطأ,
  استخراج_مفتاح_API,
} from "@/lib/prisma";
import {
  تجميع_قائمة_حجب_كاملة,
  تصنيف_أداء_التجميع,
  محاكاة_قائمة_EasyList_مصغرة,
} from "@/lib/rule-compiler";
import type { طلب_مزامنة, نتيجة_مزامنة_قائمة } from "@/types/arabic-types";

// ======================================================
// POST /api/sync — تشغيل المزامنة لقوائم محددة أو كلها
// ======================================================
export async function POST(request: NextRequest) {
  const بداية = Date.now();

  try {
    const { خطأ, رمز, مستأجر } = await التحقق_من_المستأجر(
      استخراج_مفتاح_API(request)
    );

    if (خطأ || !مستأجر) {
      return NextResponse.json(
        بناء_استجابة_خطأ(خطأ!, رمز, بداية),
        { status: رمز }
      );
    }

    let الجسم: طلب_مزامنة = {};
    try {
      الجسم = await request.json();
    } catch {
      // الجسم الفارغ مقبول - سيزامن كل القوائم
    }

    // جلب القوائم المطلوبة للمزامنة
    const قوائم = await prisma.قائمة_الحجب.findMany({
      where: {
        معرف_المستأجر: مستأجر.معرف,
        الحالة: { in: ["معلقة", "نشطة", "خطأ"] },
        ...(الجسم.معرفات_القوائم?.length && {
          معرف: { in: الجسم.معرفات_القوائم },
        }),
      },
    });

    if (قوائم.length === 0) {
      return NextResponse.json(
        بناء_استجابة_خطأ(
          "لا توجد قوائم حجب متاحة للمزامنة",
          404,
          بداية
        ),
        { status: 404 }
      );
    }

    // التحقق من عدم وجود مزامنة جارية
    const مزامنة_جارية = await prisma.سجل_مزامنة.findFirst({
      where: {
        معرف_المستأجر: مستأجر.معرف,
        حالة_المزامنة: "جارية",
        تاريخ_البدء: { gte: new Date(Date.now() - 5 * 60 * 1000) }, // آخر 5 دقائق
      },
    });

    if (مزامنة_جارية && !الجسم.إجبار_إعادة_التجميع) {
      return NextResponse.json(
        بناء_استجابة_خطأ(
          "توجد مزامنة جارية بالفعل. انتظر أو استخدم إجبار_إعادة_التجميع: true",
          409,
          بداية
        ),
        { status: 409 }
      );
    }

    // تنفيذ المزامنة لكل قائمة بالتوازي
    const نتائج_المزامنة: نتيجة_مزامنة_قائمة[] = await Promise.all(
      قوائم.map((قائمة) =>
        مزامنة_قائمة_واحدة(قائمة, مستأجر.معرف, الجسم.إجبار_إعادة_التجميع ?? false)
      )
    );

    // تجميع إحصائيات الأداء الكلية
    const ناجحة = نتائج_المزامنة.filter((ن) => ن.حالة_المزامنة === "مكتملة");
    const فاشلة = نتائج_المزامنة.filter((ن) => ن.حالة_المزامنة === "فاشلة");

    const إجمالي_قواعد_مضافة = ناجحة.reduce((م, ن) => م + ن.تفاصيل_التغييرات.قواعد_مضافة, 0);
    const إجمالي_وقت_تجميع = ناجحة.reduce((م, ن) => م + ن.مقاييس_الأداء.وقت_التجميع_مللي, 0);

    return NextResponse.json(
      بناء_استجابة(
        {
          ملخص_المزامنة: {
            إجمالي_القوائم: قوائم.length,
            ناجحة: ناجحة.length,
            فاشلة: فاشلة.length,
            إجمالي_قواعد_مضافة,
            إجمالي_وقت_تجميع_مللي: إجمالي_وقت_تجميع,
            إجمالي_وقت_عملية_المزامنة_مللي: Date.now() - بداية,
          },
          تفاصيل_القوائم: نتائج_المزامنة,
        },
        `اكتملت المزامنة: ${ناجحة.length}/${قوائم.length} قائمة ناجحة`,
        200,
        بداية
      ),
      { status: 200 }
    );
  } catch (خطأ) {
    console.error("[مزامنة:POST]", خطأ);
    return NextResponse.json(
      بناء_استجابة_خطأ("خطأ داخلي أثناء المزامنة", 500, بداية, String(خطأ)),
      { status: 500 }
    );
  }
}

// ======================================================
// GET /api/sync — جلب حالة المزامنات الأخيرة
// ======================================================
export async function GET(request: NextRequest) {
  const بداية = Date.now();

  try {
    const { خطأ, رمز, مستأجر } = await التحقق_من_المستأجر(
      استخراج_مفتاح_API(request)
    );

    if (خطأ || !مستأجر) {
      return NextResponse.json(
        بناء_استجابة_خطأ(خطأ!, رمز, بداية),
        { status: رمز }
      );
    }

    const { searchParams } = new URL(request.url);
    const حد = Math.min(parseInt(searchParams.get("حد") ?? "20"), 100);

    const سجلات = await prisma.سجل_مزامنة.findMany({
      where: { معرف_المستأجر: مستأجر.معرف },
      include: {
        القائمة: {
          select: { الاسم: true, النوع: true, رابط_المصدر: true },
        },
      },
      orderBy: { تاريخ_البدء: "desc" },
      take: حد,
    });

    const منسقة = سجلات.map((سجل) => ({
      معرف_المزامنة: سجل.معرف,
      القائمة: {
        المعرف: سجل.معرف_القائمة,
        الاسم: سجل.القائمة.الاسم,
        النوع: سجل.القائمة.النوع,
        المصدر: سجل.القائمة.رابط_المصدر,
      },
      حالة_المزامنة: سجل.حالة_المزامنة,
      تفاصيل_التغييرات: {
        قبل: سجل.قواعد_قبل,
        بعد: سجل.قواعد_بعد,
        مضافة: سجل.قواعد_مضافة,
        محذوفة: سجل.قواعد_محذوفة,
        معدلة: سجل.قواعد_معدلة,
      },
      أداء: {
        وقت_التنزيل_مللي: سجل.وقت_التنزيل_مللي,
        وقت_التجميع_مللي: سجل.وقت_التجميع_مللي,
      },
      خطأ: سجل.رسالة_الخطأ,
      الطوابع_الزمنية: {
        البدء: سجل.تاريخ_البدء.toISOString(),
        الانتهاء: سجل.تاريخ_الانتهاء?.toISOString() ?? null,
        المدة_مللي: سجل.تاريخ_الانتهاء
          ? سجل.تاريخ_الانتهاء.getTime() - سجل.تاريخ_البدء.getTime()
          : null,
      },
    }));

    return NextResponse.json(
      بناء_استجابة(منسقة, `تم جلب ${منسقة.length} سجل مزامنة`, 200, بداية),
      { status: 200 }
    );
  } catch (خطأ) {
    console.error("[مزامنة:GET]", خطأ);
    return NextResponse.json(
      بناء_استجابة_خطأ("خطأ في جلب سجلات المزامنة", 500, بداية, String(خطأ)),
      { status: 500 }
    );
  }
}

// ======================================================
// منطق المزامنة الداخلي لقائمة واحدة
// ======================================================
async function مزامنة_قائمة_واحدة(
  قائمة: { معرف: string; الاسم: string; رابط_المصدر: string; النوع: string; عدد_القواعد: number },
  معرف_المستأجر: string,
  إجبار: boolean
): Promise<نتيجة_مزامنة_قائمة> {
  const سجل = await prisma.سجل_مزامنة.create({
    data: {
      معرف_المستأجر,
      معرف_القائمة: قائمة.معرف,
      حالة_المزامنة: "جارية",
      قواعد_قبل: قائمة.عدد_القواعد,
    },
  });

  const بداية_تنزيل = Date.now();

  try {
    // =====================================================
    // مرحلة 1: تنزيل القائمة من المصدر
    // =====================================================
    let محتوى_القائمة: string;
    let ETag: string | null = null;
    let إصدار_EasyList: string | null = null;

    try {
      // في الإنتاج: تنزيل حقيقي من رابط_المصدر
      // هنا نستخدم بيانات محاكاة للتطوير
      محتوى_القائمة = محاكاة_قائمة_EasyList_مصغرة();
      ETag = `"${Date.now()}-${Math.random().toString(36).slice(2)}"`;

      // استخراج إصدار EasyList من ترويسات القائمة
      const تطابق_الإصدار = محتوى_القائمة.match(/!\s*Version:\s*(.+)/);
      إصدار_EasyList = تطابق_الإصدار?.[1]?.trim() ?? null;
    } catch (خطأ_تنزيل) {
      throw new Error(`فشل تنزيل القائمة: ${String(خطأ_تنزيل)}`);
    }

    const وقت_التنزيل = Date.now() - بداية_تنزيل;

    // =====================================================
    // مرحلة 2: تجميع القواعد وقياس الأداء
    // =====================================================
    const بداية_تجميع = Date.now();
    const نتيجة_التجميع = تجميع_قائمة_حجب_كاملة(محتوى_القائمة);
    const وقت_التجميع = Date.now() - بداية_تجميع;

    const نسبة_النجاح =
      ((نتيجة_التجميع.إحصائيات.قواعد_شبكة + نتيجة_التجميع.إحصائيات.قواعد_استثناء) /
        Math.max(نتيجة_التجميع.إحصائيات.إجمالي_الأسطر - نتيجة_التجميع.إحصائيات.تعليقات_ومسافات, 1)) * 100;

    const تصنيف_الأداء = تصنيف_أداء_التجميع(
      نتيجة_التجميع.إحصائيات.متوسط_وقت_قاعدة_مكرو,
      نسبة_النجاح
    );

    // =====================================================
    // مرحلة 3: تخزين القواعد المُجمَّعة في قاعدة البيانات
    // =====================================================
    const قواعد_للإدراج = نتيجة_التجميع.قواعد_DNR.slice(0, 5000).map((قاعدة_dnr, فهرس) => ({
      معرف_القائمة: قائمة.معرف,
      النمط: (قاعدة_dnr.condition.urlFilter ?? `rule_${فهرس}`)
        .slice(0, 500),
      نوع_القاعدة: (قاعدة_dnr.action.type === "allow"
        ? "استثناء"
        : قاعدة_dnr.action.type === "redirect"
        ? "إعادة_توجيه"
        : "حجب_شبكة") as never,
      الفئات: [] as string[],
      مُجمَّعة: true,
      رمز_DNR_المُجمَّع: JSON.stringify(قاعدة_dnr),
      وقت_التجميع_مللي: Math.round(نتيجة_التجميع.إحصائيات.متوسط_وقت_قاعدة_مكرو / 1000),
    }));

    // حذف القواعد القديمة وإدراج الجديدة (في المعاملة)
    await prisma.$transaction([
      prisma.قاعدة_تصفية.deleteMany({ where: { معرف_القائمة: قائمة.معرف } }),
      prisma.قاعدة_تصفية.createMany({ data: قواعد_للإدراج }),
      prisma.قائمة_الحجب.update({
        where: { معرف: قائمة.معرف },
        data: {
          الحالة: "نشطة",
          عدد_القواعد: نتيجة_التجميع.قواعد_DNR.length,
          حجم_الملف_بايت: BigInt(Buffer.byteLength(محتوى_القائمة, "utf8")),
          آخر_مزامنة: new Date(),
          إصدار_EasyList,
        },
      }),
    ]);

    // حفظ مقياس الأداء
    await prisma.مقياس_أداء.create({
      data: {
        معرف_المستأجر,
        عملية_التجميع: `مزامنة_${قائمة.النوع}_${قائمة.معرف}`,
        حجم_الإدخال_بايت: BigInt(Buffer.byteLength(محتوى_القائمة, "utf8")),
        عدد_القواعد_المُعالَجة: نتيجة_التجميع.إحصائيات.إجمالي_الأسطر,
        وقت_التجميع_كلي_مللي: وقت_التجميع,
        متوسط_وقت_قاعدة_مكرو: نتيجة_التجميع.إحصائيات.متوسط_وقت_قاعدة_مكرو,
        استهلاك_الذاكرة_KB: Math.round(process.memoryUsage().heapUsed / 1024),
        نسبة_ضغط_القواعد:
          نتيجة_التجميع.قواعد_DNR.length /
          Math.max(نتيجة_التجميع.إحصائيات.إجمالي_الأسطر, 1),
        قواعد_DNR_المُنتجة: نتيجة_التجميع.قواعد_DNR.length,
        تحذيرات_التحقق: نتيجة_التجميع.تحذيرات,
        حالة_النجاح: true,
      },
    });

    // تحديث سجل المزامنة إلى مكتملة
    await prisma.سجل_مزامنة.update({
      where: { معرف: سجل.معرف },
      data: {
        حالة_المزامنة: "مكتملة",
        قواعد_بعد: نتيجة_التجميع.قواعد_DNR.length,
        قواعد_مضافة: Math.max(0, نتيجة_التجميع.قواعد_DNR.length - قائمة.عدد_القواعد),
        قواعد_محذوفة: Math.max(0, قائمة.عدد_القواعد - نتيجة_التجميع.قواعد_DNR.length),
        وقت_التنزيل_مللي: وقت_التنزيل,
        وقت_التجميع_مللي: وقت_التجميع,
        ETag,
        تاريخ_الانتهاء: new Date(),
      },
    });

    return {
      معرف_المزامنة: سجل.معرف,
      معرف_القائمة: قائمة.معرف,
      اسم_القائمة: قائمة.الاسم,
      حالة_المزامنة: "مكتملة",
      تفاصيل_التغييرات: {
        قواعد_مضافة: Math.max(0, نتيجة_التجميع.قواعد_DNR.length - قائمة.عدد_القواعد),
        قواعد_محذوفة: Math.max(0, قائمة.عدد_القواعد - نتيجة_التجميع.قواعد_DNR.length),
        قواعد_معدلة: 0,
        الإجمالي_قبل: قائمة.عدد_القواعد,
        الإجمالي_بعد: نتيجة_التجميع.قواعد_DNR.length,
        نسبة_التغيير:
          قائمة.عدد_القواعد > 0
            ? Math.round(
                (Math.abs(نتيجة_التجميع.قواعد_DNR.length - قائمة.عدد_القواعد) /
                  قائمة.عدد_القواعد) *
                  100
              )
            : 100,
      },
      مقاييس_الأداء: {
        وقت_التنزيل_مللي: وقت_التنزيل,
        وقت_التجميع_مللي: وقت_التجميع,
        إجمالي_الوقت_مللي: وقت_التنزيل + وقت_التجميع,
      },
      المصدر: {
        الرابط: قائمة.رابط_المصدر,
        ETag,
        إصدار_EasyList,
      },
      الطابع_الزمني: new Date().toISOString(),
    };
  } catch (خطأ) {
    const رسالة_الخطأ = String(خطأ);

    await prisma.سجل_مزامنة.update({
      where: { معرف: سجل.معرف },
      data: {
        حالة_المزامنة: "فاشلة",
        رسالة_الخطأ,
        تاريخ_الانتهاء: new Date(),
      },
    });

    await prisma.قائمة_الحجب.update({
      where: { معرف: قائمة.معرف },
      data: { الحالة: "خطأ" },
    });

    return {
      معرف_المزامنة: سجل.معرف,
      معرف_القائمة: قائمة.معرف,
      اسم_القائمة: قائمة.الاسم,
      حالة_المزامنة: "فاشلة",
      تفاصيل_التغييرات: {
        قواعد_مضافة: 0,
        قواعد_محذوفة: 0,
        قواعد_معدلة: 0,
        الإجمالي_قبل: قائمة.عدد_القواعد,
        الإجمالي_بعد: قائمة.عدد_القواعد,
        نسبة_التغيير: 0,
      },
      مقاييس_الأداء: {
        وقت_التنزيل_مللي: Date.now() - بداية_تنزيل,
        وقت_التجميع_مللي: 0,
        إجمالي_الوقت_مللي: Date.now() - بداية_تنزيل,
      },
      المصدر: {
        الرابط: قائمة.رابط_المصدر,
        ETag: null,
        إصدار_EasyList: null,
      },
      خطأ: رسالة_الخطأ,
      الطابع_الزمني: new Date().toISOString(),
    };
  }
}
