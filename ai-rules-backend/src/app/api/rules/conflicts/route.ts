export async function POST(request: NextRequest) {
  const بداية = Date.now();

  try {
    const { القواعد } = await request.json();

    if (!Array.isArray(القواعد) || القواعد.length < 2) {
      return NextResponse.json({
        خطأ: "مطلوب مصفوفة من قاعدتين على الأقل",
        رمز: 400,
      }, { status: 400 });
    }

    // اكتشاف سريع بدون AI للأنماط الواضحة
    const تعارضات_سريعة = اكتشاف_تعارضات_سريع(القواعد);

    // تحليل عميق بالـ AI
    const استجابة = await fetch("https://api.anthropic.com/v1/messages", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        model: "claude-sonnet-4-6",
        max_tokens: 1000,
        system: SYSTEM_PROMPT,
        messages: [{
          role: "user",
          content: `حلل هذه القواعد واكتشف التعارضات والمشاكل:\n\n${القواعد.slice(0, 50).join("\n")}`,
        }],
      }),
    });

    const بيانات = await استجابة.json();
    const نص = بيانات.content
      .map((c: { type: string; text?: string }) => c.type === "text" ? c.text : "")
      .join("")
      .replace(/```json|```/g, "")
      .trim();

    const تحليل_AI = JSON.parse(نص);

    return NextResponse.json({
      الحالة: "نجاح",
      البيانات: {
        ...تحليل_AI,
        اكتشافات_فورية: تعارضات_سريعة,
        إحصائيات_التحليل: {
          عدد_القواعد_المُحللة: القواعد.length,
          وقت_التحليل_مللي: Date.now() - بداية,
          مستوى_التحليل: "AI + قواعد ثابتة",
        },
      },
      الطابع_الزمني: new Date().toISOString(),
    });

  } catch (خطأ) {
    return NextResponse.json({
      الحالة: "خطأ",
      الرسالة: String(خطأ),
      رمز: 500,
    }, { status: 500 });
  }
}

// ======================================================
// اكتشاف سريع بدون AI
// ======================================================
function اكتشاف_تعارضات_سريع(قواعد: string[]) {
  const تعارضات: Array<{ نوع: string; قاعدة_1: string; قاعدة_2: string; وصف: string }> = [];
  const تكرارات: string[][] = [];
  const مجموعة = new Set<string>();

  for (const قاعدة of قواعد) {
    const منظفة = قاعدة.trim();

    // اكتشاف التكرار الحرفي
    if (مجموعة.has(منظفة)) {
      تكرارات.push([منظفة, منظفة]);
    }
    مجموعة.add(منظفة);

    // اكتشاف الاستثناء مقابل الحجب
    if (منظفة.startsWith("@@")) {
      const النمط_الأصلي = منظفة.slice(2);
      if (قواعد.includes(النمط_الأصلي)) {
        تعارضات.push({
          نوع: "حجب_واستثناء_متعارضان",
          قاعدة_1: النمط_الأصلي,
          قاعدة_2: منظفة,
          وصف: "قاعدة تحجب النطاق وأخرى تستثنيه بالضبط",
        });
      }
    }
  }

  return { تعارضات_مكتشفة: تعارضات.length, تكرارات_مكتشفة: تكرارات.length, تعارضات, تكرارات };
}
